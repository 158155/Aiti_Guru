-- ОПТИМИЗАЦИЯ 3 (представления):
--Предложение по созданию материализованного представления

-- Вместо обычного view можно создать материализованное представление (materialized view). Обычный view не хранит данные, а каждый раз заново выполняет тяжелый рекурсивный запрос. Материализованное представление сохраняет результат запроса на диске, как обычную таблицу. Это позволяет читать данные мгновенно, без рекурсий и сложных соединений.
-- Для отчета "Топ-5 товаров за месяц" имеет смысл создать материализованное представление, которое обновляется, например, раз в час. Пока данные не устарели, любой запрос к отчету будет работать за миллисекунды, независимо от размера таблиц заказов и количества товаров.

-- Преимущества
-- Скорость чтения становится очень высокой. Даже при тысячах заказов в день отчет будет открываться мгновенно.
-- Можно создавать индексы прямо на материализованном представлении, что ускоряет сортировку и фильтрацию.
-- Нагрузка на базу данных снижается, потому что тяжелый рекурсивный запрос перестает выполняться при каждом обращении к отчету.

-- Недостатки
-- Данные в отчете всегда немного устаревшие. Если заказы поступают непрерывно, отчет покажет результат на момент последнего обновления.
-- Требуется регулярно обновлять представление, например, через планировщик задач или фоновую задачу в приложении.
-- Материализованное представление занимает место на диске, так как хранит копию данных.
-- Обновление представления (REFRESH) само по себе выполняет тяжелый запрос и может ненадолго нагружать базу данных.